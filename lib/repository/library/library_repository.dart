  /// Get all folders for the current user, ordered by order_index
  Future<List<LibraryFolder>> getFolders() => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    final response = await supabase
        .from('user_folders')
        .select()
        .eq('user_id', userId)
        .order('order_index', ascending: true);

    return (response as List)
        .map((json) => LibraryFolder.fromSupabase(json))
        .toList();
  });

  /// Get a single folder by ID
  Future<LibraryFolder?> getFolder(String folderId) => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    final response =
        await supabase
            .from('user_folders')
            .select()
            .eq('id', folderId)
            .eq('user_id', userId)
            .maybeSingle();

    return response != null ? LibraryFolder.fromSupabase(response) : null;
  });

  /// Create a new folder or database node.
  Future<LibraryFolder> createFolder({
    required String name,
    String? color,
    String? icon,
    int? orderIndex,
    String? parentId,
    String nodeType = LibraryFolder.nodeTypeFolder,
  }) => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    // If no order index provided, get the next available one
    final nextOrder = orderIndex ?? await _getNextFolderOrder();

    final folder = LibraryFolder(
      id: '', // Will be generated by database
      userId: userId,
      name: name,
      color: color ?? '#0FB4E5',
      icon: icon ?? 'folder',
      orderIndex: nextOrder,
      parentId: parentId,
      nodeType: nodeType,
      createdAt: DateTime.now(),
      updatedAt: DateTime.now(),
    );

    final response =
        await supabase
            .from('user_folders')
            .insert(folder.toSupabaseInsert())
            .select()
            .single();

    return LibraryFolder.fromSupabase(response);
  });

  /// Ensure the user has a default organizational folder and database.
  /// Folders organize databases; only databases contain games.
  Future<void> ensureDefaultFolders() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) return;

    // Always make sure the special Liked Games folder exists, independent of
    // whether the user already has user-created folders.
    await ensureLikedGamesFolder();

    // Check if user already has any non-special folders
    final existing = await supabase
        .from('user_folders')
        .select('id')
        .eq('user_id', userId)
        .eq('is_liked_games', false)
        .limit(1);

    if ((existing as List).isNotEmpty) return;

    final rootFolder = await createFolder(name: 'My Database');
    await createFolder(
      name: 'My Folder',
      parentId: rootFolder.id,
      nodeType: LibraryFolder.nodeTypeDatabase,
    );
  }

  /// Returns the user's special "Liked Games" folder, creating it on first
  /// access. Identical to any other folder mechanically — same UI, same
  /// `user_saved_analyses` rows. The `is_liked_games` flag (partial unique
  /// index per user) marks it as the destination for board double-tap likes.
  Future<LibraryFolder> ensureLikedGamesFolder() => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    final existing =
        await supabase
            .from('user_folders')
            .select()
            .eq('user_id', userId)
            .eq('is_liked_games', true)
            .maybeSingle();

    if (existing != null) {
      return LibraryFolder.fromSupabase(existing);
    }

    final nextOrder = await _getNextFolderOrder();
    final response =
        await supabase
            .from('user_folders')
            .insert({
              'user_id': userId,
              'name': 'My Likes',
              'color': '#F5453A',
              'icon': 'liked',
              'order_index': nextOrder,
              'is_liked_games': true,
            })
            .select()
            .single();
    return LibraryFolder.fromSupabase(response);
  });

