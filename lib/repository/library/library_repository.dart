  /// Update a saved analysis
  Future<SavedAnalysis> updateSavedAnalysis(SavedAnalysis analysis) =>
      handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

