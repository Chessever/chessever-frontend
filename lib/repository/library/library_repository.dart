  /// Returns the subset of [sourceGameIds] already present in a folder.
  Future<Set<String>> getExistingSourceGameIdsInFolder({
    required String folderId,
    required List<String> sourceGameIds,
  }) => handleApiCall(() async {
    if (sourceGameIds.isEmpty) return <String>{};
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    // Keep chunk size moderate to avoid very large `IN (...)` payloads.
    const chunkSize = 250;
    final existing = <String>{};

    for (var i = 0; i < sourceGameIds.length; i += chunkSize) {
      final end = (i + chunkSize).clamp(0, sourceGameIds.length);
      final chunk = sourceGameIds.sublist(i, end);
      final response = await supabase
          .from('user_saved_analyses')
          .select('source_game_id')
          .eq('user_id', userId)
          .eq('folder_id', folderId)
          .inFilter('source_game_id', chunk);

      for (final row in (response as List)) {
        final id = row['source_game_id']?.toString();
        if (id != null && id.isNotEmpty) existing.add(id);
      }
    }

    return existing;
  });

