  /// Get count of analyses in a folder
  Future<int> getAnalysisCountInFolder(String folderId) =>
      handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) return 0;

    final response = await supabase
        .from('user_saved_analyses')
        .select()
        .eq('user_id', userId)
        .eq('folder_id', folderId)
        .count();

    return response.count;
  });

  // ============ STREAM SUBSCRIPTIONS ============

  /// Stream folder changes for real-time updates
  Stream<List<LibraryFolder>> subscribeFolders() {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) {
      return Stream.error(Exception('User not authenticated'));
    }

    return supabase
        .from('user_folders')
        .stream(primaryKey: ['id'])
        .eq('user_id', userId)
        .order('order_index')
        .map(
          (data) =>
              data.map((json) => LibraryFolder.fromSupabase(json)).toList(),
        );
  }

  /// Stream analyses in a folder for real-time updates
  Stream<List<SavedAnalysis>> subscribeAnalyses({String? folderId}) {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) {
      return Stream.error(Exception('User not authenticated'));
    }

    final streamBuilder = supabase
        .from('user_saved_analyses')
        .stream(primaryKey: ['id'])
        .eq('user_id', userId);

    // Filter in memory since stream doesn't support conditional eq()
    return streamBuilder.order('created_at', ascending: false).map((data) {
      final analyses =
          data.map((json) => SavedAnalysis.fromSupabase(json)).toList();

      if (folderId != null) {
        return analyses.where((a) => a.folderId == folderId).toList();
      }
      return analyses;
    });
  }
}
