                              onTap: () {
                                setState(() {
                                  _isCreatingNewFolder = true;
                                  _selectedFolder = null;
                                  _folderErrorMessage = null;
                                });
                                HapticFeedback.selectionClick();
                                // Focus new folder name field
                                Future.delayed(const Duration(milliseconds: 100), () {
                                  if (mounted) {
                                    _newFolderNameFocusNode.requestFocus();
                                  }
                                });
                              },
                            ),
                          ),
                        ],
                      ),
                      SizedBox(height: 12.h),
                      // Show appropriate UI based on selection
                      if (!_isCreatingNewFolder)
                        foldersAsync.when(
                          data: (folders) => _buildFolderSelector(folders),
                          loading: () => _buildFolderSelectorLoading(),
                          error: (e, _) => _buildFolderSelectorError(e),
                        )
                      else
                        _buildNewFolderInput(),
                    ],
                  ),
                ),

                // Error messages
                if (_errorMessage != null || _folderErrorMessage != null) ...[
                  SizedBox(height: 16.h),
                  Padding(
                    padding: EdgeInsets.symmetric(horizontal: 20.w),
                    child: Container(
                      padding: EdgeInsets.all(12.sp),
                      decoration: BoxDecoration(
                        color: kRedColor.withValues(alpha: 0.1),
                        borderRadius: BorderRadius.circular(8.br),
                        border: Border.all(
                          color: kRedColor.withValues(alpha: 0.3),
                        ),
                      ),
                      child: Row(
                        children: [
                          Icon(
                            Icons.error_outline,
                            color: kRedColor,
                            size: 18.sp,
                          ),
                          SizedBox(width: 8.w),
                          Expanded(
                            child: Text(
                              _errorMessage ?? _folderErrorMessage ?? '',
                              style: AppTypography.textSmRegular.copyWith(
                                color: kRedColor,
                              ),
                            ),
                          ),
                        ],
                      ),
                    ),
                  ),
                ],

                SizedBox(height: 24.h),

                // Action buttons
                Padding(
                  padding: EdgeInsets.symmetric(horizontal: 20.w),
                  child: Row(
                    children: [
                      // Cancel button
                      Expanded(
                        child: OutlinedButton(
                          onPressed: _isSaving
                              ? null
                              : () {
                                HapticFeedback.lightImpact();
                                Navigator.of(context).pop();
                              },
                          style: OutlinedButton.styleFrom(
                            foregroundColor: kWhiteColor,
                            side: BorderSide(
                              color: kWhiteColor.withValues(alpha: 0.2),
                            ),
                            shape: RoundedRectangleBorder(
                              borderRadius: BorderRadius.circular(8.br),
                            ),
                            padding: EdgeInsets.symmetric(vertical: 14.h),
                          ),
                          child: Text(
                            'Cancel',
                            style: AppTypography.textSmMedium,
                          ),
                        ),
                      ),
                      SizedBox(width: 12.w),
                      // Save button
                      Expanded(
                        flex: 2,
                        child: ElevatedButton(
                          onPressed: _isSaving ? null : _handleSave,
                          style: ElevatedButton.styleFrom(
                            backgroundColor: kPrimaryColor,
                            foregroundColor: kWhiteColor,
                            disabledBackgroundColor:
                                kPrimaryColor.withValues(alpha: 0.5),
                            shape: RoundedRectangleBorder(
                              borderRadius: BorderRadius.circular(8.br),
                            ),
                            padding: EdgeInsets.symmetric(vertical: 14.h),
                            elevation: 0,
                          ),
                          child: _isSaving
                              ? SizedBox(
                                width: 20.w,
                                height: 20.h,
                                child: CircularProgressIndicator(
                                  strokeWidth: 2,
                                  valueColor: AlwaysStoppedAnimation<Color>(
                                    kWhiteColor,
                                  ),
                                ),
                              )
                              : Text(
                                'Save Analysis',
                                style: AppTypography.textSmBold,
                              ),
                        ),
                      ),
                    ],
                  ),
                ),

                SizedBox(height: 20.h),

                // Bottom padding for safe area
                SizedBox(height: MediaQuery.of(context).viewInsets.bottom),
              ],
            ),
          ),
    );
  }

  Widget _buildToggleOption({
    required String label,
    required bool isSelected,
    required VoidCallback onTap,
  }) {
    return GestureDetector(
      onTap: _isSaving ? null : onTap,
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 200),
        curve: Curves.easeInOut,
        padding: EdgeInsets.symmetric(vertical: 10.h),
        decoration: BoxDecoration(
          color: isSelected
              ? kPrimaryColor.withValues(alpha: 0.15)
              : kWhiteColor.withValues(alpha: 0.04),
          borderRadius: BorderRadius.circular(8.br),
          border: Border.all(
            color: isSelected
                ? kPrimaryColor.withValues(alpha: 0.6)
                : Colors.transparent,
            width: 1.5,
          ),
        ),
        child: Center(
          child: Text(
            label,
            style: AppTypography.textSmMedium.copyWith(
              color: isSelected
                  ? kPrimaryColor
                  : kWhiteColor.withValues(alpha: 0.7),
            ),
          ),
        ),
      ),
    );
  }

