  Widget _buildFolderSelector(List<LibraryFolder> folders) {
    return Container(
      padding: EdgeInsets.symmetric(horizontal: 16.w, vertical: 4.h),
      decoration: BoxDecoration(
        color: kWhiteColor.withValues(alpha: 0.06),
        borderRadius: BorderRadius.circular(8.br),
      ),
      child: DropdownButton<LibraryFolder?>(
        value: _selectedFolder,
        isExpanded: true,
        dropdownColor: kBlack2Color,
        underline: const SizedBox.shrink(),
        icon: Icon(
          Icons.keyboard_arrow_down,
          color: kWhiteColor.withValues(alpha: 0.7),
          size: 20.sp,
        ),
        hint: Text(
          'No folder (save to root)',
          style: AppTypography.textSmRegular.copyWith(
            color: kWhiteColor.withValues(alpha: 0.7),
          ),
        ),
        style: AppTypography.textSmRegular.copyWith(
          color: kWhiteColor,
        ),
        items: [
          DropdownMenuItem<LibraryFolder?>(
            value: null,
            child: Text(
              'No folder',
              style: AppTypography.textSmRegular.copyWith(
                color: kWhiteColor.withValues(alpha: 0.7),
              ),
            ),
          ),
          ...folders.map((folder) {
            return DropdownMenuItem<LibraryFolder?>(
              value: folder,
              child: Row(
                children: [
                  Container(
                    width: 8.w,
                    height: 8.h,
                    decoration: BoxDecoration(
                      color: _parseColorString(folder.color),
                      shape: BoxShape.circle,
                    ),
                  ),
                  SizedBox(width: 12.w),
                  Expanded(
                    child: Text(
                      folder.name,
                      style: AppTypography.textSmRegular.copyWith(
                        color: kWhiteColor,
                      ),
                      overflow: TextOverflow.ellipsis,
                    ),
                  ),
                ],
              ),
            );
          }),
        ],
        onChanged: _isSaving
            ? null
            : (LibraryFolder? folder) {
              setState(() {
                _selectedFolder = folder;
              });
              HapticFeedback.selectionClick();
            },
      ),
    );
  }

