                    ],
                  ),
                ),

                SizedBox(height: 24.h),

                // Title input
                Padding(
                  padding: EdgeInsets.symmetric(horizontal: 20.w),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text(
                        'Title',
                        style: AppTypography.textSmMedium.copyWith(
                          color: kWhiteColor.withValues(alpha: 0.9),
