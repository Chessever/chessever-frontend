                      ),
                      SizedBox(height: 8.h),
                      TextField(
                        controller: _titleController,
                        focusNode: _titleFocusNode,
                        enabled: !_isSaving,
                        maxLength: 100,
                        style: AppTypography.textMdRegular.copyWith(
                          color: kWhiteColor,
